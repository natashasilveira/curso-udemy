// Operadores Aritméticos
const num1 = 10;
const num2 = 5;
let resultado;

// adição e concatenação: +
resultado = num1 + num2;
console.log(resultado);
resultado = '10' + 5;
console.log(resultado);

// subtração: -
resultado = num1 - num2;
console.log(resultado);

// multiplicação: *
resultado = num1 * num2;
console.log(resultado);

// potenciação: **
resultado = num1 ** num2;
console.log(resultado);

// divisão: /
resultado = num1 / num2;
console.log(resultado);

// resto da divisão: %
resultado = num1 % num2;
console.log(resultado);

// incrementa: ++
resultado++;
++resultado;

// decrementa: --
resultado--;
--resultado;

resultado += 2; //incrementa 2 ao resultado